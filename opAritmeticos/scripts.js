/*
  Operadores Aritméticos
*/

// multiplicação *
console.log("multiplicação: ", 3.2 * 5.5)

// divisão /
console.log("divisão: ", 12 / 2)

// soma + 
console.log("soma: ", 34 + 67)

// subtração -
console.log("subtração: ", 34 - 23)

// resto da divisão %
let remainder 
remainder = 11 % 3;
console.log("resto da divisão: ", remainder)

// incremento ++
let increment = 0;
console.log("incrementando depois: ", increment++)
console.log(increment)

console.log("incrementando antes: ", ++increment)
console.log(increment)

//decremento --
let decrement = 1;
console.log("decrementando depois: ", decrement--)
console.log(decrement)

console.log("decrementando antes: ", --decrement)
console.log(decrement)